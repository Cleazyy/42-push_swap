#include "push_swap.h"

static void	rotate(t_stack **stack)
{
	t_stack	*head;
	t_stack	*temp;

	if (*stack && (*stack)->next)
	{
		head = *stack;
		*stack = (*stack)->next;
		temp = *stack;
		while (temp->next)
			temp = temp->next;
		temp->next = head;
		head->next = NULL;
	}
}

void	ra(t_stack **stack_a)
{
	rotate(stack_a);
	write(1, "ra\n", 3);
}

void	rb(t_stack **stack_b)
{
	rotate(stack_b);
	write(1, "rb\n", 3);
}

void	rr(t_stack **stack_a, t_stack **stack_b)
{
	rotate(stack_a);
	rotate(stack_b);
	write(1, "rr\n", 3);
}
